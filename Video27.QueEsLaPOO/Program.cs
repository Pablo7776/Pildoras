//Paradigmas de la programación:

//    1)Programación orientada a procedimientos
//Desventajas:
//    *Unidades de código muy grandes en las aplicaciones complejas
//    *En aplicaciones complejas el código resultaba dificil de desifrar
//    *Poco reutilizable
//    *Si existe fallo en alguna linea del código, es muy probable que el programa caiga
//    *Aparición frecuente de código espaguti, generado sobre todo por instrucciones de tipo goTo y goSu, esto dificultaba seguir el hilo del programa
//    *Difícil de depurar por otros programadores en caso de necesidad o error

//    2)Programación orientada a objetos


//En qué consiste?
//            Transladar la naturaleza de los objetos de la vida real al código de programación
//        Cuál es la naturaleza de un objeto de la vida real?
//            Los objetos tienen un estado, un comportamiento(Qué puede hacer?), y unas propiedades
//        Pongamos un ejemplo: El objeto coche
//            *Cuál es el estado de un coche? Un coche puede estar parado, circulando, aparcado, etc
//            *Qué propiedades tiene un coche? Un coche tiene un color, un peso, un tamaño, etc
//            *Que comportamiento tiene un coche? Un coche puede arrancar, frenar, acelerar, girar, etc

//        Ventajas:
//            *Programas divididos en "trozos", "partes", "modulos", o "clases". Modularización
//            *Muy reutilizable. Herencia.
//            *Si existe fallo en alguna dínea del código, el programa continuará con su funcionamiento. Trataminto de excepciones.
//            *Encapsulamiento.

//        Vocabulario de la POO:
//            *Clase
//            *objeto
//            *Ejemplar de la clase. Instancia de la clase. Ejemplarizar una clase. Instanciar una clase
//            *Modularización
//            *Encapsulamiento / encapsulación
//            *Herencia
//            *Polimorfismo 

